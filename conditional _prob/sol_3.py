# Fruit freshness checker.

Fruit =input("Name of the fruit :")
Color =input("Color of the fruit:")

if Fruit=="Banana":

    if Color=="Yellow":
        print("ripe")
    elif Color=="Green":
        print("unripe")
    elif Color=="Brown":
        print("overripe")

if Fruit=="Litchi":

    if Color=="Yellow":
        print("ripe")
    elif Color=="Green":
        print("unripe")
    elif Color=="Red":
        print("Delicious")


